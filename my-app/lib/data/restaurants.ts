// Helper functions
export function getRestaurantBySlug(slug: string): Restaurant | undefined {
  return restaurants.find((restaurant) => restaurant.slug === slug);
}

export function getAllRestaurantSlugs(): string[] {
  return restaurants.map((restaurant) => restaurant.slug);
}

export function getRestaurantsByCity(city: string): Restaurant[] {
  return restaurants.filter((restaurant) => restaurant.city.toLowerCase() === city.toLowerCase());
}

export function getRestaurantsByCuisine(cuisine: string): Restaurant[] {
  return restaurants.filter((restaurant) => 
    restaurant.cuisineType.toLowerCase().includes(cuisine.toLowerCase())
  );
}

export function getRestaurantsByPriceRange(priceRange: Restaurant["priceRange"]): Restaurant[] {
  return restaurants.filter((restaurant) => restaurant.priceRange === priceRange);
}

export function getParrillas(): Restaurant[] {
  return restaurants.filter((restaurant) => 
    restaurant.cuisineType === "Parrilla"
  ).sort((a, b) => (a.ranking || 999) - (b.ranking || 999));
}

export function getTopRestaurants(limit: number = 10): Restaurant[] {
  return restaurants
    .filter((restaurant) => restaurant.ranking && restaurant.ranking <= limit)
    .sort((a, b) => (a.ranking || 999) - (b.ranking || 999));
}

export function getRestaurantsByNeighborhood(neighborhood: string): Restaurant[] {
  return restaurants.filter((restaurant) => 
    restaurant.neighborhood.toLowerCase() === neighborhood.toLowerCase()
  );
}