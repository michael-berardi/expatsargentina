export function getProvinceBySlug(slug: string): Province | undefined {
  return provinces.find((p) => p.slug === slug);
}

export function getCityBySlug(slug: string): City | undefined {
  return cities.find((c) => c.slug === slug);
}

export function getProvincesByRegion(region: Region): Province[] {
  return provinces.filter((p) => p.region === region);
}

export function getCitiesByProvince(provinceSlug: string): City[] {
  return cities.filter((c) => c.provinceSlug === provinceSlug);
}

export function getRegionColor(region: string): string {
  const r = regions.find((r) => r.name === region);
  return r?.color ?? "bg-muted-foreground";
}
