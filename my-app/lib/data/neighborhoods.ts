// Helper functions
export function getNeighborhoodBySlug(slug: string): Neighborhood | undefined {
  return neighborhoods.find((n) => n.slug === slug);
}

export function getAllNeighborhoodSlugs(): string[] {
  return neighborhoods.map((n) => n.slug);
}

export function getNeighborhoodsByZone(zone: Neighborhood["zone"]): Neighborhood[] {
  return neighborhoods.filter((n) => n.zone === zone);
}

export function getNeighborhoodsBySafety(minSafety: Neighborhood["safety"]): Neighborhood[] {
  const safetyOrder: Neighborhood["safety"][] = ["Excellent", "Very Good", "Good", "Moderate", "Caution"];
  const minIndex = safetyOrder.indexOf(minSafety);
  return neighborhoods.filter((n) => safetyOrder.indexOf(n.safety) <= minIndex);
}
